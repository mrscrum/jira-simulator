class TestIssueTransitions:
    @pytest.mark.asyncio
    async def test_transition_to_in_progress_and_done(self, jira):
        # Create a fresh issue
        issue = await jira.create_issue(
            project_key=PROJECT_KEY,
            issue_type="Task",
            summary="[SIM-TEST] Transition lifecycle test",
            fields={},
        )
        issue_key = issue["key"]
        print(f"  Created: {issue_key}")

        # Get available transitions
        transitions = await jira.get_issue_transitions(issue_key)
        t_names = {t["name"]: t["id"] for t in transitions}
        print(f"  Available transitions: {list(t_names.keys())}")

        # Transition to In Progress
        in_progress_id = t_names.get("In Progress")
        if in_progress_id:
            await jira.transition_issue(issue_key, in_progress_id)
            updated = await jira.get_issue(issue_key)
            status = updated["fields"]["status"]["name"]
            print(f"  After transition: {status}")
            assert status == "In Progress"

            # Get transitions from In Progress
            transitions2 = await jira.get_issue_transitions(issue_key)
            t_names2 = {t["name"]: t["id"] for t in transitions2}
            print(f"  Transitions from In Progress: {list(t_names2.keys())}")

            # Transition to Done
            done_id = t_names2.get("Done")
            if done_id:
                await jira.transition_issue(issue_key, done_id)
                done_issue = await jira.get_issue(issue_key)
                done_status = done_issue["fields"]["status"]["name"]
                print(f"  Final status: {done_status}")
                assert done_status == "Done"
        else:
            print("  WARNING: No 'In Progress' transition found")
            pytest.skip("In Progress transition not available")


