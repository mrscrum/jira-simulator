class TestSprintOperations:
    @pytest.mark.asyncio
    async def test_sprint_lifecycle(self, jira):
        board = await jira.get_board(PROJECT_KEY)
        if not board:
            pytest.skip("No board found for PHX")
        board_id = board["id"]
        print(f"  Board: {board_id}")

        # Check for existing active sprint
        active = await jira.get_active_sprint(board_id)
        if active:
            print(f"  Active sprint exists: {active['name']} (id={active['id']})")

        # Create a new sprint
        from datetime import UTC, datetime, timedelta

        now = datetime.now(UTC)
        sprint = await jira.create_sprint(
            board_id=board_id,
            name=f"SIM-TEST Sprint {now.strftime('%H:%M')}",
            start_date=now,
            end_date=now + timedelta(days=14),
        )
        print(f"  Created sprint: {sprint['id']} - {sprint['name']}")
        assert "id" in sprint

        # Create issues and add to sprint
        issue = await jira.create_issue(
            project_key=PROJECT_KEY,
            issue_type="Story",
            summary="[SIM-TEST] Sprint story",
            fields={},
        )
        await jira.add_issues_to_sprint(sprint["id"], [issue["key"]])
        print(f"  Added {issue['key']} to sprint {sprint['id']}")


class TestBacklogGeneration:
    """Test generating backlog content and creating issues in Jira."""

    @pytest.mark.asyncio
    async def test_generate_and_create_in_jira(self, jira):
        from app.engine.backlog import TemplateContentGenerator, generate_issues

        issues = await generate_issues(
            count=3,
            team_name="Phoenix",
            content_generator=TemplateContentGenerator(),
        )
        assert len(issues) == 3

        created_keys = []
        for issue_data in issues:
            created = await jira.create_issue(
                project_key=PROJECT_KEY,
                issue_type=issue_data["issue_type"],
                summary=f"[SIM] {issue_data['summary']}",
                fields={
                    "description": {
                        "type": "doc",
                        "version": 1,
                        "content": [
                            {
                                "type": "paragraph",
                                "content": [
                                    {
                                        "type": "text",
                                        "text": issue_data["description"],
                                    }
                                ],
                            }
                        ],
                    }
                },
            )
            created_keys.append(created["key"])
            print(
                f"  Created {issue_data['issue_type']}: {created['key']} "
                f"- {issue_data['summary'][:50]}... ({issue_data['story_points']}pts)"
            )

        assert len(created_keys) == 3
        print(f"  All 3 backlog issues created: {created_keys}")
